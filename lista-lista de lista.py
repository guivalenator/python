#Recorre una lista unidimensional vertical
for i in range(1,7):
    print(i)

#Agrega una linea
print('\n')

#Recorre una lista agregando un espacio entre los elementos unidimensional horizontal
for i in range(1,7):
    print(i, end=' ')

#Agrega una linea
print('\n')

#Recorre una lista de lista bidimensiona√±
for i in range(1,7):
    for j in range(1,7):
        print(0, end='  ')
    print('\n')

# Muestra coordenadas de una lista bidimensional
for i in range(1,7):
    for j in range(1,7):
        print(f'({i},{j})', end='  ')
    print('\n')    

